var color = 'yellow';
//if else condition diye
if (color == 'blue') {
    console.log('color is blue');

}
else if (color == 'green') {
    console.log('color is green');
}

else if (color == 'yellow') {
    console.log('color is yellow');
}

else if (color == 'red') {
    console.log('color is red');
}
else {
    console.log('color is black');
}

//switch diye 
switch (color) {
    case 'blue':
        console.log('color is blue');
        break;
    case 'red':
        console.log('color is red');
        break;
    case 'green':
        console.log('color is green');
        break;
    case 'white':
        console.log('color is white');
        break;
    case 'yellow':
        console.log('color is yellow');
        break;

    default:
        console.log('color is black');
}

